const CATEGORIES = [
  { label: "Внешность", range: [0, 25], emoji: "✨" },
  { label: "Ум", range: [25, 50], emoji: "🧠" },
  { label: "Душа", range: [50, 75], emoji: "💖" },
  { label: "Таланты", range: [75, 101], emoji: "🌟" },
];

export default function ComplimentsSection() {
  const [revealed, setRevealed] = useState<Set<number>>(new Set());
  const [activeCategory, setActiveCategory] = useState(0);

  const [start, end] = CATEGORIES[activeCategory].range;
  const visible = COMPLIMENTS.slice(start, end);

  const toggleReveal = (idx: number) => {
    setRevealed(prev => {
      const next = new Set(prev);
      if (next.has(idx)) { next.delete(idx); } else { next.add(idx); }
      return next;
    });
  };

  const revealAll = () => {
    const allIdx = visible.map((_, i) => start + i);
    setRevealed(prev => {
      const next = new Set(prev);
      allIdx.forEach(i => next.add(i));
      return next;
    });
  };

  return (
    <div className="max-w-2xl mx-auto w-full">
      {/* Category tabs */}
      <div className="flex gap-2 justify-center flex-wrap mb-10">
        {CATEGORIES.map((cat, i) => (
          <button
            key={i}
            onClick={() => setActiveCategory(i)}
            className="px-5 py-2 rounded-full font-montserrat text-xs font-semibold tracking-widest uppercase transition-all duration-300"
            style={{
              background: activeCategory === i
                ? "linear-gradient(135deg, #a855f7, #ec4899)"
                : "rgba(255,255,255,0.05)",
              color: activeCategory === i ? "#fff" : "rgba(255,255,255,0.4)",
              border: `1px solid ${activeCategory === i ? "transparent" : "rgba(255,255,255,0.08)"}`,
            }}
          >
            {cat.emoji} {cat.label}
          </button>
        ))}
      </div>

      {/* Reveal all button */}
      <div className="text-center mb-8">
        <button
          onClick={revealAll}
          className="px-6 py-2 rounded-full font-montserrat text-xs tracking-widest uppercase transition-all duration-300 hover:scale-105"
          style={{
            background: "rgba(168,85,247,0.1)",
            color: "#c084fc",
            border: "1px solid rgba(168,85,247,0.3)",
          }}
        >
          Открыть все
        </button>
      </div>

      {/* Compliments grid */}
      <div className="grid gap-3">
        {visible.map((text, i) => {
          const globalIdx = start + i;
          const isOpen = revealed.has(globalIdx);
          return (
            <div
              key={globalIdx}
              onClick={() => toggleReveal(globalIdx)}
              className="cursor-pointer rounded-2xl transition-all duration-500 overflow-hidden"
              style={{
                background: isOpen
                  ? "linear-gradient(135deg, rgba(168,85,247,0.15), rgba(236,72,153,0.1))"
                  : "rgba(255,255,255,0.04)",
                border: `1px solid ${isOpen ? "rgba(168,85,247,0.35)" : "rgba(255,255,255,0.06)"}`,
                transform: isOpen ? "scale(1.01)" : "scale(1)",
              }}
            >
              <div className="flex items-center gap-4 p-4">
                <div
                  className="w-8 h-8 rounded-full flex items-center justify-center flex-shrink-0 font-montserrat text-xs font-bold"
                  style={{
                    background: isOpen
                      ? "linear-gradient(135deg, #a855f7, #ec4899)"
                      : "rgba(255,255,255,0.08)",
                    color: isOpen ? "#fff" : "rgba(255,255,255,0.3)",
                  }}
                >
                  {globalIdx + 1}
                </div>
                {isOpen ? (
                  <p className="font-cormorant italic text-white/90 text-lg leading-snug flex-1">
                    {text}
                  </p>
                ) : (
                  <p className="font-montserrat text-white/25 text-xs tracking-widest uppercase flex-1">
                    Нажми, чтобы открыть ✨
                  </p>
                )}
              </div>
            </div>
          );
        })}
      </div>

      {/* Counter */}
      <div className="text-center mt-10">
        <p className="font-montserrat text-white/25 text-xs tracking-widest uppercase">
          Открыто {revealed.size} из {COMPLIMENTS.length}
        </p>
      </div>
    </div>
  );
}